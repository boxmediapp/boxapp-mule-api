package uk.co.boxnetwork.data;

public class DataReport {
  private Long numberOfEpisodes;
  
  private Double numberOfHoursTotal;
  private Double numberOfHoursActive;  

public Long getNumberOfEpisodes() {
	return numberOfEpisodes;
}

public void setNumberOfEpisodes(Long numberOfEpisodes) {
	this.numberOfEpisodes = numberOfEpisodes;
}

public Double getNumberOfHoursTotal() {
	return numberOfHoursTotal;
}

public void setNumberOfHoursTotal(Double numberOfHoursTotal) {
	this.numberOfHoursTotal = numberOfHoursTotal;
}

public Double getNumberOfHoursActive() {
	return numberOfHoursActive;
}

public void setNumberOfHoursActive(Double numberOfHoursActive) {
	this.numberOfHoursActive = numberOfHoursActive;
}

  
}
